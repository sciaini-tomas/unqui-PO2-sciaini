package ar.edu.unq.po2.tp3;

public class Punto {
	private float x;
	private float y;
	
	public Punto(float x,float y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	// Getter y Setter X
	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	//////////////////////////
	
	//Getter y setter Y
	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}
	////////////////////////////
}
